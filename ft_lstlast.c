#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	i;
	int	j;

	j = 0;
	i = ft_lstsize(lst);
	while (j < i - 1)
	{
		lst = lst->next;
		j++;
	}
	return (lst);
}
